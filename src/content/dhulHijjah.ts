/**
 * يومُ عرفة (التاسع): أعظمُ أيّام العشر. صيامُه لغير الحاجّ يُكفّر سنتين.
 * «صيامُ يوم عرفة أحتسبُ على الله أن يُكفّر السنةَ التي قبله والسنةَ التي
 * بعده» — رواه مسلم (١١٦٢) عن أبي قتادة. (والحاجُّ لا يصومه.)
 */
export const dhulHijjahArafah = `🤲 يومُ عرفة

اليومُ يومُ عرفة، أعظمُ أيّام العام؛ وصيامُه لغير الحاجّ «يُكفّر السنةَ التي قبله والسنةَ التي بعده» كما أخبر النبيُّ ﷺ (رواه مسلم).
اجعَلوه يومَ دعاءٍ وسكينةٍ في البيت: صُم من يُطيق برفق، وادعُ لأبنائك واحدًا واحدًا بأسمائهم، وعلِّمهم أن يرفعوا أيديهم معك.
يومٌ يتعلّم فيه طفلك أنّ لله بابًا مفتوحًا لا يُرَدُّ عنه، فيطمئنّ قلبُه.`;

/**
 * يختار تذكيرَ العشر ليومٍ بعينه: يومُ عرفة (التاسع) رسالتُه الخاصّة،
 * وما قبله (١..٨) يدور في مجموعة الأيّام بيومِ الشهر الهجريّ. نقيّةٌ
 * (تأخذ now/tz)؛ والافتراضُ الآن + config.timezone كي يستهلكها schedules.ts
 * عبر `content: () => pickDhulHijjahContent()`.
 */
export function pickDhulHijjahContent(
  now: Date = new Date(),
  tz: string = config.timezone,
): string {
  const { day } = hijriDate(now, tz);
  if (day === 9) return dhulHijjahArafah;
  return dhulHijjahDays[(day - 1) % dhulHijjahDays.length];
}
