/**
 * رسالةُ العيد: بطاقةٌ واحدةٌ دافئة تُنشَر صباحَ العيد فقط — عيدِ الفطر
 * (١ شوّال) أو عيدِ الأضحى (١٠ ذي الحجة) — تهنّئ الأسرة وتُذكّرها أن
 * تجعل العيدَ فرحةً وعبادةً يألفها الأبناء، فيرتبط الدِّينُ في قلوبهم
 * بالبهجة والأمان. يُنشَر بصمت، ويُضبَط يومُه بـ isEidDay في
 * src/seasons.ts؛ ويختار pickEidContent البطاقةَ المناسبة بالشهر الهجريّ.
 *
 * منهج المحتوى (انظر CLAUDE.md): ما نُسِب إلى النبيّ ﷺ فهو صحيحٌ أو حسن،
 * وتحقيقُه في تعليقٍ فوقه؛ والنصُّ بين «...» لفظٌ حرفيٌّ تُحُقِّق منه،
 * وما عداه معنًى بلا تنصيص. نصٌّ بلا parse_mode، والإيموجي في أوّل السطر.
 *
 * ملاحظة: لا حديثَ صحيحًا يخصّ «هديةَ العيد» باسمها، فأسندنا الإكرامَ إلى
 * أصلٍ عامّ ثابت: «تهادَوا تحابُّوا». وصيغةُ التكبير عن ابن مسعود موقوفًا
 * بإسنادٍ صحيح، لا مرفوعةً، فعُرِضت تعليمًا بلا نسبةٍ إلى لفظٍ نبويّ.
 *
 * ⚠️ تبقى مراجعةُ طالب علمٍ موثوقٍ مرّةً واحدة هي الضمانُ الأخير.
 */
import { isEidAlFitr } from '../seasons';
import { config } from '../config';

/**
 * عيدُ الفطر. الأدلّة في المتن:
 *   • فرحُ العيد: «إنّ لكلِّ قومٍ عيدًا، وهذا عيدنا» — متّفق عليه (البخاري
 *     ٩٥٢، مسلم ٨٩٢) عن عائشة، في قصّة الجاريتين تُغنّيان يومَ عيد، و«دعهما»
 *     من لفظ البخاري. (جمعناهما بنقاطِ حذفٍ «...» لأنهما من رواية البخاري.)
 *   • أكلُ تمراتٍ وترًا قبل صلاة الفطر — رواه البخاري (٩٥٣) عن أنس (معنًى
 *     بلا تنصيص).
 *   • «تهادَوا تحابُّوا» — رواه البخاري في الأدب المفرد (٥٩٤)، وحسّنه الألباني.
 */
export const eidAlFitr = `🌙 عيدُ فطرٍ مبارك

تقبّل الله منّا ومنكم صالحَ الأعمال، وأعاده عليكم وعلى أبنائكم بالخير واليُمن والبركات 🤍

اجعَلوا فرحةَ العيد لأبنائكم ذكرى دافئة:
🕌 كبِّروا معهم: «الله أكبر، الله أكبر، لا إله إلا الله، والله أكبر، الله أكبر، ولله الحمد».
🌅 ومن سنّته ﷺ أن يأكل تمراتٍ وِترًا قبل صلاة الفطر (رواه البخاري)؛ فابدؤوا صباحكم بها معًا.
🎈 وافسَحوا لهم في اللعب والمرح؛ فالعيدُ فرحٌ مشروع، قال ﷺ لأبي بكرٍ حين أنكر غناءَ جاريتين صغيرتين يومَ عيد: «دعهما... إنّ لكلِّ قومٍ عيدًا، وهذا عيدنا» (رواه البخاري).
🎁 أكرِموهم بهديّةٍ وكِسوة، وصِلوا بهم الأرحام؛ فالهديّةُ تزرع المحبّة: «تهادَوا تحابُّوا» (رواه البخاري في الأدب المفرد وحسّنه الألباني).

طفلٌ يفرح بعيده في حضن أهله، ويراهم يفرحون به، ينشأ وقلبُه عامرٌ بالأمان 🤍`;

/**
 * عيدُ الأضحى. الأدلّة في المتن:
 *   • الأضحية: «ضحّى النبيُّ ﷺ بكبشين أملحين أقرنين، ذبحهما بيده، وسمّى
 *     وكبّر» — متّفق عليه (البخاري ٥٥٦٥، مسلم ١٩٦٦) عن أنس. (أملح: أبيضُ
 *     يخالطه سواد؛ أقرن: ذو قرنين.)
 *   • أيّامُ التشريق: «أيّامُ التشريق أيّامُ أكلٍ وشربٍ وذكرٍ لله» — رواه
 *     مسلم (١١٤١) عن نُبيشة. (واللفظُ التامّ «…وذكرٍ لله عز وجل».)
 */
export const eidAlAdha = `🕋 عيدُ أضحى مبارك

تقبّل الله منّا ومنكم، وأقرّ أعينَكم بأبنائكم، وبارك لكم في عيدكم 🤍

اجعَلوا العيدَ فرحةً وعبادةً يتعلّمها أبناؤكم:
🕌 كبِّروا معهم: «الله أكبر، الله أكبر، لا إله إلا الله، والله أكبر، الله أكبر، ولله الحمد».
🐑 ومن سنّة العيد الأضحية؛ «ضحّى النبيُّ ﷺ بكبشين أملحين أقرنين، ذبحهما بيده، وسمّى وكبّر» (متّفق عليه). أشرِكوا أبناءكم في توزيع لحمها على الفقراء والجيران، وجنِّبوهم ما يُفزعهم؛ فيتعلّمون أنّ العيد عطاءٌ ورحمة.
🍖 وأيّامُ التشريق بعده «أيّامُ أكلٍ وشربٍ وذكرٍ لله» (رواه مسلم)؛ فوسِّعوا على أهلكم، واذكروا الله، وصِلوا أرحامكم.
🎁 وافرِحوا أبناءكم بالهديّة والكِسوة واللعب؛ فالطفلُ الذي يفرح بعيده في كنف أهله ينشأ مطمئنًّا، يحبّ دينَه الذي جعل له أعيادًا تجمعه بمن يحبّ 🤍`;

/**
 * يختار بطاقةَ العيد بالشهر الهجريّ: عيدُ الفطر في شوّال، وإلا فعيدُ
 * الأضحى (لا يُستدعى إلا في يومِ عيدٍ عبر skipIf، انظر schedules.ts).
 * نقيّةٌ (تأخذ now/tz)؛ والافتراضُ الآن + config.timezone.
 */
export function pickEidContent(now: Date = new Date(), tz: string = config.timezone): string {
  return isEidAlFitr(now, tz) ? eidAlFitr : eidAlAdha;
}
