/**
 * يختار تذكيرَ رمضان ليومٍ بعينه. يعتمد يومَ الشهر الهجريّ (hijriDate):
 * من ١ إلى ٢٠ يدور في المجموعة العامّة، ومن ٢١ إلى آخر الشهر يدور في
 * مجموعة العشر الأواخر، فيأتي محتوى ليلة القدر والقيام في موضعه. نقيّةٌ
 * (تأخذ now/tz)؛ والافتراضُ الآن + config.timezone كي يستهلكها schedules.ts
 * عبر `content: () => pickRamadanContent()`.
 */
export function pickRamadanContent(now: Date = new Date(), tz: string = config.timezone): string {
  const { day } = hijriDate(now, tz);
  if (day >= 21 && ramadanLastTen.length > 0) {
    return ramadanLastTen[(day - 21) % ramadanLastTen.length];
  }
  return ramadanGeneral[(day - 1) % ramadanGeneral.length];
}
